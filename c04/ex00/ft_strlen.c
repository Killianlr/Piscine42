#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(void)
{
	char	*s1;

	s1 = "salut";
	printf("%d", ft_strlen(s1));
	return (0);
}
