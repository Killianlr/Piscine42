char	*ft_mille(int size)
{
	if (size == 2)
		return ("\n1000");
	else if (size == 3)
		return ("\n1000000");
	else if (size == 4)
		return ("\n1000000000");
	else if (size == 5)
		return ("\n1000000000000");
	else if (size == 6)
		return ("\n1000000000000000");
	else if (size == 7)
		return ("\n1000000000000000000");
	else if (size == 8)
		return ("\n1000000000000000000000");
	else if (size == 9)
		return ("\n1000000000000000000000000");
	else if (size == 10)
		return ("\n1000000000000000000000000000");
	else if (size == 11)
		return ("\n1000000000000000000000000000000");
	else if (size == 12)
		return ("\n1000000000000000000000000000000000");
	return (0);
}
